import turtle

def rectangle(m,n,w,h,c):
    """
    This draws a rectangle
    """
    turtle.penup()
    turtle.goto(m,n)
    turtle.pendown()
    turtle.pencolor(c)
    turtle.fillcolor(c)
    turtle.begin_fill()
    turtle.forward(w)
    turtle.left(90)
    turtle.forward(h)
    turtle.left(90)
    turtle.forward(w)
    turtle.left(90)
    turtle.forward(h)
    turtle.left(90)
    turtle.end_fill()

def frosting(m,n,w,h,c):
    """
    This draws a frosting
    """
    turtle.penup()
    turtle.goto(m,n)
    turtle.pendown()
    turtle.pencolor(c)
    turtle.fillcolor(c)
    turtle.begin_fill()
    turtle.forward(w)
    turtle.right(90)
    turtle.forward(h)
    turtle.right(90)
    turtle.forward(w)
    turtle.right(90)
    turtle.forward(h)
    turtle.right(90)
    turtle.end_fill()  

def draw_candle(x,y,h,c):
    """
    this will draw candle
    """
    turtle.penup()
    turtle.goto(x, y)
    turtle.pendown()
    turtle.pencolor("black")
    turtle.fillcolor(c)
    turtle.begin_fill()
    turtle.setheading(90) 
    turtle.forward(h)
    turtle.right(90)
    turtle.forward(7) 
    turtle.right(90)
    turtle.forward(h)
    turtle.right(90)
    turtle.forward(7)
    turtle.right(90)
    turtle.end_fill()
    turtle.penup()
    turtle.forward(h)
    turtle.pendown()
    turtle.fillcolor("orange")
    turtle.begin_fill()
    turtle.setheading(60)
    turtle.forward(9)
    turtle.setheading(300)
    turtle.forward(9)
    turtle.setheading(180)
    turtle.forward(9)
    turtle.end_fill()

def toppings(x,y,c):
    """
    this will draw the toppings of the cake
    """
    turtle.penup()
    turtle.goto(x, y)
    turtle.pendown()
    turtle.setheading(90)
    turtle.pencolor(c)
    turtle.fillcolor(c)
    turtle.begin_fill()
    turtle.circle(10, 180)
    turtle.end_fill()

