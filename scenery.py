def draw_balloon(x,y,c):
    """
    the will draw the balloons
    """
    turtle.speed(0)
    turtle.pencolor("black")
    turtle.penup()
    turtle.goto(x,y-30)
    turtle.pendown()
    turtle.setheading(90)
    turtle.forward(80)
    turtle.setheading(0)
    turtle.fillcolor(c)
    turtle.begin_fill()
    turtle.circle(50)
    turtle.end_fill()

